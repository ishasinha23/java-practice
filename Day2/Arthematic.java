public class Arthematic {
  public static void main (String[] args)
  {
    int a=10;
    int b=40;
    int sum=a+b;
    System.out.println(sum);
    int diff=b-a;
    System.out.println(diff);
    int mul=a*b;
    System.out.println(mul);
    int div=a/b;
    System.out.println(div);
    int module=a%b;
    System.out.println(module);
  }
}
